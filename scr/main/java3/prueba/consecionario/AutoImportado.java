package prueba.consecionario;

public class AutoImportado extends Auto {

	protected int códigoImportación;
	protected int valorDeLaImportacion;

	public int getCódigoImportación() {
		return códigoImportación;
	}

	public void setCódigoImportación(int códigoImportación) {
		this.códigoImportación = códigoImportación;
	}

	public int getValorDeLaImportacion() {
		return valorDeLaImportacion;
	}

	public void setValorDeLaImportacion(int valorDeLaImportacion) {
		this.valorDeLaImportacion = valorDeLaImportacion;
	}

}
